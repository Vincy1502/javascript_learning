let cgpa=5

if(cgpa>5 && cgpa<=10){
    if(cgpa>5.0 && cgpa<=6){
        console.log("E grade")
    }
    else if(cgpa>6.0 && cgpa<=7){
        console.log("D grade")
    }
    else if(cgpa>7.0 && cgpa<=8){
        console.log("C grade")
    }
    else if(cgpa>8.0 && cgpa<=9){
        console.log("B grade")
    }
    else if(cgpa>9.0 && cgpa<=10){
        console.log("A grade")
    }
}
else{
    console.log("fail")
}